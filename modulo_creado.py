# Vamos a crear un modulo en este fichero
# Para definir un fichero creado como modulo se usa la palabra reservada def

#Creamos algunas operaciones basicas que hara este modulo
def suma(a, b):
    return a + b

def resta(a, b):
    return a - b

def multiplicacion(a, b):
    return a * b

def division(a, b):
    return a / b
